package edu.handong.csee.java.hw2;

/**
 * class Util
 */
public class Util {
    /**
     * 입력된 string형 변수를 모두 대문자로 변경하여 return
     * @param stringValue string형 변수
     * @return 대문자로 변경된 string형 변수를 return
     */
    static String getUppercaseString(String stringValue) {
        return stringValue.toUpperCase();
    }
    
}
